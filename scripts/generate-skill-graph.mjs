#!/usr/bin/env node
/**
 * generate-skill-graph.mjs
 *
 * Scans every SKILL.md and extracts cross-references to OTHER skills.
 * Produces src/lib/skill-graph.generated.ts — a nodes+edges graph.
 *
 * Reference patterns it picks up:
 *   - `skill-name` (backtick-quoted, kebab-case)
 *   - "Triggered by → some-skill" (arrows)
 *   - "calls skill-name"
 *   - "uses skill-name"
 *   - "via skill-name"
 *
 * Only counts a reference if the target name exists in the registry.
 */

import fs from "node:fs/promises";
import path from "node:path";
import { existsSync } from "node:fs";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), "..");
const SEEDS = path.join(ROOT, "docs/seeds/skills");
const OUT = path.join(ROOT, "src/lib/skill-graph.generated.ts");

async function loadSkillNames() {
  const names = new Set();
  for (const e of await fs.readdir(SEEDS, { withFileTypes: true })) {
    if (!e.isDirectory()) continue;
    if (e.name.startsWith("_")) continue;
    if (existsSync(path.join(SEEDS, e.name, "SKILL.md"))) {
      names.add(e.name);
    }
  }
  return names;
}

async function extractReferences(skillName, allNames) {
  const filePath = path.join(SEEDS, skillName, "SKILL.md");
  const text = await fs.readFile(filePath, "utf8");
  const refs = new Map(); // target → count

  // Strip the growth-hook section (every skill has same boilerplate)
  const main = text.split(/^## Growth hook/m)[0] || text;

  // Pattern 1: backtick-quoted skill names: `some-skill`
  const backtickRegex = /`([a-z][a-z0-9-]+)`/g;
  let m;
  while ((m = backtickRegex.exec(main))) {
    const ref = m[1];
    if (ref && ref !== skillName && allNames.has(ref)) {
      refs.set(ref, (refs.get(ref) || 0) + 1);
    }
  }

  // Pattern 2: arrow refs: "→ skill-name"
  const arrowRegex = /[→↦⟶]\s*`?([a-z][a-z0-9-]+)`?/g;
  while ((m = arrowRegex.exec(main))) {
    const ref = m[1];
    if (ref && ref !== skillName && allNames.has(ref)) {
      refs.set(ref, (refs.get(ref) || 0) + 1);
    }
  }

  // Pattern 3: action verbs + skill name
  const actionRegex = /(?:calls?|invokes?|uses?|via|routes to)\s+`?([a-z][a-z0-9-]+)`?/gi;
  while ((m = actionRegex.exec(main))) {
    const ref = m[1];
    if (ref && ref !== skillName && allNames.has(ref)) {
      refs.set(ref, (refs.get(ref) || 0) + 1);
    }
  }

  return refs;
}

async function main() {
  const allNames = await loadSkillNames();
  console.log(`Scanning ${allNames.size} skills...`);

  const nodes = [];
  const edges = [];

  for (const name of [...allNames].sort()) {
    nodes.push({ id: name });
    const refs = await extractReferences(name, allNames);
    for (const [target, weight] of refs) {
      edges.push({ source: name, target, weight });
    }
  }

  // Compute degrees
  const inDegree = new Map();
  const outDegree = new Map();
  for (const e of edges) {
    inDegree.set(e.target, (inDegree.get(e.target) || 0) + 1);
    outDegree.set(e.source, (outDegree.get(e.source) || 0) + 1);
  }
  for (const n of nodes) {
    n.inDegree = inDegree.get(n.id) || 0;
    n.outDegree = outDegree.get(n.id) || 0;
  }

  // Sort hubs (most connected)
  const hubs = [...nodes]
    .map((n) => ({ ...n, total: n.inDegree + n.outDegree }))
    .sort((a, b) => b.total - a.total)
    .slice(0, 20);

  // Sort orphans (isolated, no in or out)
  const orphans = nodes.filter((n) => n.inDegree === 0 && n.outDegree === 0);

  const ts = `// AUTO-GENERATED by scripts/generate-skill-graph.mjs
// Do NOT edit by hand. Re-run after modifying any SKILL.md.
// Generated: ${new Date().toISOString()}

export interface GraphNode {
  id: string;
  inDegree: number;
  outDegree: number;
}

export interface GraphEdge {
  source: string;
  target: string;
  weight: number;
}

export const NODES: readonly GraphNode[] = ${JSON.stringify(nodes, null, 2)};

export const EDGES: readonly GraphEdge[] = ${JSON.stringify(edges, null, 2)};

export const NODE_COUNT = ${nodes.length};
export const EDGE_COUNT = ${edges.length};

/** Top 20 most-connected skills */
export const HUBS: readonly { id: string; total: number; inDegree: number; outDegree: number }[] = ${JSON.stringify(
    hubs.map((h) => ({
      id: h.id,
      total: h.total,
      inDegree: h.inDegree,
      outDegree: h.outDegree,
    })),
    null,
    2
  )};

/** Skills with no inbound or outbound references (potential cleanup) */
export const ORPHANS: readonly string[] = ${JSON.stringify(
    orphans.map((o) => o.id),
    null,
    2
  )};
`;

  await fs.mkdir(path.dirname(OUT), { recursive: true });
  await fs.writeFile(OUT, ts, "utf8");

  console.log(`[graph] wrote ${OUT}`);
  console.log(`  nodes: ${nodes.length}`);
  console.log(`  edges: ${edges.length}`);
  console.log(`  hubs (top 5): ${hubs.slice(0, 5).map((h) => h.id).join(", ")}`);
  console.log(`  orphans: ${orphans.length}`);
}

main().catch((err) => {
  console.error("[graph] FATAL:", err);
  process.exit(1);
});
