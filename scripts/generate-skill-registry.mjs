#!/usr/bin/env node
/**
 * generate-skill-registry.mjs
 *
 * Scans every SKILL.md in docs/seeds/skills/ and produces a
 * TypeScript registry at src/lib/skill-registry.generated.ts.
 *
 * The registry is import-anywhere — used by skill-runtime + dispatch
 * for fast O(1) lookup by name or trigger.
 *
 * Run: node scripts/generate-skill-registry.mjs
 * Re-run automatically: pre-build, pre-deploy, or when SKILL.md count changes.
 */

import fs from "node:fs/promises";
import path from "node:path";
import { existsSync } from "node:fs";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), "..");
const SEEDS = path.join(ROOT, "docs/seeds/skills");
const OUT = path.join(ROOT, "src/lib/skill-registry.generated.ts");

function parseFrontmatter(text) {
  const m = text.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return {};
  const fm = {};
  for (const line of m[1].split("\n")) {
    const kv = line.match(/^(\w+):\s*(.*)$/);
    if (kv) fm[kv[1]] = kv[2].trim();
  }
  const triggersBlock = m[1].match(/triggers:\n((?:\s+-\s+.*\n?)+)/);
  if (triggersBlock) {
    fm.triggers = triggersBlock[1]
      .split("\n")
      .map((l) => l.match(/-\s+"?([^"]+?)"?$/))
      .filter(Boolean)
      .map((x) => x[1].trim());
  } else {
    fm.triggers = [];
  }
  return fm;
}

async function loadAll() {
  const skills = [];
  for (const e of await fs.readdir(SEEDS, { withFileTypes: true })) {
    if (!e.isDirectory()) continue;
    if (e.name.startsWith("_")) continue;
    const skillFile = path.join(SEEDS, e.name, "SKILL.md");
    if (!existsSync(skillFile)) continue;
    const text = await fs.readFile(skillFile, "utf8");
    const fm = parseFrontmatter(text);
    const pack = fm.pack || "";
    const KNOWN_META = new Set([
      "meta-growth-watcher",
      "growth-cluster-gap-detector",
      "growth-skill-spec-generator",
      "recursive-growth-throttle",
    ]);
    const skillName = fm.name || e.name;
    const isMeta =
      fm.is_meta === "true" ||
      pack === "meta-growth" ||
      pack === "meta" ||
      KNOWN_META.has(skillName);
    skills.push({
      name: fm.name || e.name,
      description: fm.description || "",
      triggers: fm.triggers || [],
      isMeta,
      pack,
      pathRelative: `docs/seeds/skills/${e.name}/SKILL.md`,
    });
  }
  skills.sort((a, b) => a.name.localeCompare(b.name));
  return skills;
}

function emitTs(skills) {
  const header = `// AUTO-GENERATED by scripts/generate-skill-registry.mjs
// Do NOT edit by hand. Re-run the script after modifying any SKILL.md.
// Generated: ${new Date().toISOString()}
// Skill count: ${skills.length}

export interface SkillEntry {
  name: string;
  description: string;
  triggers: readonly string[];
  isMeta: boolean;
  pack: string;
  pathRelative: string;
}

export const SKILLS: readonly SkillEntry[] = ${JSON.stringify(skills, null, 2)} as const;

export const SKILL_COUNT = ${skills.length};
export const META_SKILL_COUNT = ${skills.filter((s) => s.isMeta).length};
export const DOMAIN_SKILL_COUNT = ${skills.filter((s) => !s.isMeta).length};

// O(1) lookup by name
const _byName = new Map<string, SkillEntry>();
for (const s of SKILLS) _byName.set(s.name, s);

export function findSkill(name: string): SkillEntry | undefined {
  return _byName.get(name);
}

// Find skills whose triggers match an input (case-insensitive substring).
// Used by inbound-classifier and dispatcher.
export function findSkillsByTrigger(input: string): SkillEntry[] {
  const lower = input.toLowerCase();
  const matches: SkillEntry[] = [];
  for (const s of SKILLS) {
    for (const t of s.triggers) {
      if (lower.includes(t.toLowerCase()) || t.toLowerCase().includes(lower)) {
        matches.push(s);
        break;
      }
    }
  }
  return matches;
}

// All skill names — for diagnostics + completion.
export const SKILL_NAMES: readonly string[] = SKILLS.map((s) => s.name);
`;
  return header;
}

async function main() {
  const skills = await loadAll();
  const ts = emitTs(skills);
  await fs.mkdir(path.dirname(OUT), { recursive: true });
  await fs.writeFile(OUT, ts, "utf8");
  console.log(
    `[registry] wrote ${OUT} — ${skills.length} skills (${skills.filter((s) => s.isMeta).length} meta, ${skills.filter((s) => !s.isMeta).length} domain)`
  );
}

main().catch((err) => {
  console.error("[registry] FATAL:", err);
  process.exit(1);
});
