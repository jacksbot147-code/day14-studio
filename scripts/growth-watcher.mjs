// ---- escape MarkdownV2 for Telegram ----
function escapeMd(text) {
  return String(text).replace(/([_*[\]()~`>#+\-=|{}.!\\])/g, "\\$1");
}

// ---- queue Telegram approval card ----
async function queueTelegramApproval(candidate, draftName) {
  const env = await loadEnv();
  const chatId = env.TELEGRAM_CHAT_ID;
  if (!chatId) return; // Telegram not wired yet — silently skip

  const filename = `${Date.now()}-growth-${draftName}.json`;
  const filepath = path.join(TELEGRAM_OUTBOX, filename);

  const text =
    `🌱 *Growth\\-watcher: new skill candidate*\n\n` +
    `Action observed ${candidate.occurrences.length}× across ${candidate.contexts.length} contexts:\n` +
    `_${escapeMd(candidate.phrase.slice(0, 80))}_\n\n` +
    `Draft: \`${escapeMd(draftName)}\`\n` +
    `Location: \`_drafts/${escapeMd(draftName)}/SKILL\\.md\`\n\n` +
    `Review + promote when ready\\.`;

  await fs.writeFile(
    filepath,
    JSON.stringify(
      {
        chat_id: chatId,
        text,
        parse_mode: "MarkdownV2",
        urgency: "P3",
        queued_at: new Date().toISOString(),
        sent_at: null,
      },
      null,
      2
    )
  );
}

// ---- append to growth log ----
async function appendGrowthLog(candidate, draftName) {
  const line = `\n## ${new Date().toISOString()} — \`${draftName}\`\n\n- Phrase: ${candidate.phrase}\n- Occurrences: ${candidate.occurrences.length}\n- Contexts: ${candidate.contexts.length} (${candidate.contexts.join(", ")})\n- Draft: \`docs/seeds/skills/_drafts/${draftName}/SKILL.md\`\n- Status: pending Jack's review\n`;
  await fs.appendFile(GROWTH_LOG, line, "utf8");
}

// ---- append to meta-gaps log (recursive layer) ----
async function appendMetaGapsLog(candidate, draftName, risk) {
  const line = `\n## ${new Date().toISOString()} — \`${draftName}\` (meta)\n\n- Phrase: ${candidate.phrase}\n- Occurrences: ${candidate.occurrences.length}\n- Growth-cluster contexts: ${candidate.contexts.length} (${candidate.contexts.join(", ")})\n- Recurrence risk: ${risk.toFixed(2)}\n- Draft: \`docs/seeds/skills/_drafts/_meta/${draftName}/SKILL.md\`\n- Status: pending Jack's review (P2 — meta layer)\n`;
  await fs.appendFile(META_GAPS_LOG, line, "utf8");
}

// ---- queue Telegram approval for a META draft (P2, distinct copy) ----
async function queueTelegramApprovalMeta(candidate, draftName, risk) {
  const env = await loadEnv();
  const chatId = env.TELEGRAM_CHAT_ID;
  if (!chatId) return;

  const filename = `${Date.now()}-meta-growth-${draftName}.json`;
  const filepath = path.join(TELEGRAM_OUTBOX, filename);

  const riskBadge =
    risk < 0.3 ? "🟢" : risk < 0.5 ? "🟡" : risk < 0.7 ? "🟠" : "🔴";

  const text =
    `🧬 *Meta\\-growth: skill candidate from inside the growth cluster*\n\n` +
    `${riskBadge} Recurrence risk: ${escapeMd(risk.toFixed(2))}\n` +
    `Pattern observed ${candidate.occurrences.length}× across ${candidate.contexts.length} growth components:\n` +
    `_${escapeMd(candidate.phrase.slice(0, 80))}_\n\n` +
    `Draft: \`${escapeMd(draftName)}\`\n` +
    `Location: \`_drafts/_meta/${escapeMd(draftName)}/SKILL\\.md\`\n\n` +
    (risk >= 0.7
      ? `⚠️ HIGH risk — Jack classifies before promote\\.`
      : `Review carefully — meta\\-skills shape architecture\\.`);

  await fs.writeFile(
    filepath,
    JSON.stringify(
      {
        chat_id: chatId,
        text,
        parse_mode: "MarkdownV2",
        urgency: "P2", // meta layer = more visible than domain P3
        queued_at: new Date().toISOString(),
        sent_at: null,
      },
      null,
      2
    )
  );
}

// ---- compute recurrence_risk (mirror of draftMetaSkillMd's logic) ----
function computeRecurrenceRisk(phrase) {
  const text = phrase.toLowerCase();
  let risk = 0.1;
  if (text.includes("skill") || text.includes("draft")) risk += 0.3;
  if (text.includes("watcher") || text.includes("growth")) risk += 0.2;
  if (text.includes("rule") || text.includes("threshold")) risk += 0.2;
  return Math.min(1.0, risk);
}

// ---- write the draft file ----
async function writeDraft(candidate, options = {}) {
  const isMeta = options.isMeta === true;
  const name = generateSkillName(candidate.phrase);
  if (!name || name.length < 3) {
    await log(`Skipping candidate with too-short name: "${candidate.phrase}"`);
    return null;
  }

  const targetDir = isMeta ? META_DRAFTS_DIR : DRAFTS_DIR;
  const draftDir = path.join(targetDir, name);
  const draftPath = path.join(draftDir, "SKILL.md");

  if (existsSync(draftPath)) {
    await log(`Draft already exists for "${name}" (meta=${isMeta}), skipping`);
    return null;
  }

  await fs.mkdir(draftDir, { recursive: true });
  const content = isMeta
    ? draftMetaSkillMd(candidate)
    : draftSkillMd(candidate);
  await fs.writeFile(draftPath, content, "utf8");

  return name;
}

