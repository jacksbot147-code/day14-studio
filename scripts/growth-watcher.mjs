// ---- main loop ----
async function scanCycle() {
  try {
    const seen = await loadSeen();
    const entries = await readRegister();

    if (entries.length === 0) return;

    // ---- PASS 1: domain (is_meta=false), 2-occurrence rule ----
    const domainGroups = groupAdHocByPhrase(entries, false);
    const domainCandidates = detectCandidates(
      domainGroups,
      seen.proposed_phrases,
      2
    );

    if (domainCandidates.length > 0) {
      await log(`Domain pass: ${domainCandidates.length} candidate(s)`);
      for (const candidate of domainCandidates) {
        const draftName = await writeDraft(candidate);
        if (!draftName) continue;

        await appendGrowthLog(candidate, draftName);
        await queueTelegramApproval(candidate, draftName);
        seen.proposed_phrases.push(candidate.normalized);

        await log(`Drafted domain candidate: ${draftName}`);
      }
    }

    // ---- PASS 2: meta (is_meta=true), 3-occurrence rule, throttled ----
    const metaGroups = groupAdHocByPhrase(entries, true);
    const metaCandidates = detectCandidates(
      metaGroups,
      seen.proposed_phrases,
      3
    );

    if (metaCandidates.length > 0) {
      await log(`Meta pass: ${metaCandidates.length} candidate(s) detected`);

      for (const candidate of metaCandidates) {
        // Throttle gate — caps + circuit breaker
        const gate = await throttleAllowMetaDraft();
        if (!gate.allow) {
          await log(
            `🔴 meta-draft BLOCKED by throttle (${gate.reason}) — phrase: "${candidate.phrase}"`
          );
          // Don't add to seen — we want to retry once the throttle re-opens
          continue;
        }

        const risk = computeRecurrenceRisk(candidate.phrase);

        // recurrence_risk > 0.7 → block automatic draft; surface only
        if (risk > 0.7) {
          await log(
            `🔴 meta-draft GATED — risk ${risk.toFixed(2)} > 0.7 for "${candidate.phrase}". Surfacing to Jack without auto-drafting.`
          );
          await appendMetaGapsLog(candidate, "(gated, no draft)", risk);
          // Still notify, but as a gated alert
          await queueTelegramApprovalMeta(candidate, "(gated)", risk);
          seen.proposed_phrases.push(candidate.normalized);
          continue;
        }

        const draftName = await writeDraft(candidate, { isMeta: true });
        if (!draftName) continue;

        await appendMetaGapsLog(candidate, draftName, risk);
        await queueTelegramApprovalMeta(candidate, draftName, risk);
        seen.proposed_phrases.push(candidate.normalized);

        await log(
          `Drafted meta candidate: ${draftName} (risk=${risk.toFixed(2)})`
        );
      }
    }

    await saveSeen(seen);
  } catch (err) {
    await log(`scanCycle error: ${err.message}`);
  }
}

// ---- main ----
async function main() {
  await ensureDirs();
  await log(`Growth-watcher starting (poll ${POLL_INTERVAL_MS / 1000}s)`);
  await log(`Register: ${REGISTER_PATH}`);
  await log(`Drafts:   ${DRAFTS_DIR}`);

  await log(`Meta-drafts: ${META_DRAFTS_DIR}`);
  await log(`Circuit: ${META_CIRCUIT_FILE}`);

  setInterval(scanCycle, POLL_INTERVAL_MS);
  setInterval(heartbeat, HEARTBEAT_INTERVAL_MS);

  await scanCycle();
  await heartbeat();

  await log("Growth-watcher running (domain + meta passes active).");
}

main().catch(async (err) => {
  await log(`FATAL: ${err.message}`);
  process.exit(1);
});
