#!/usr/bin/env node
/**
 * growth-watcher.mjs
 *
 * Day14 OS — self-expanding skill system, always-on mode.
 *
 * Long-running daemon that:
 *   1. Polls the work-register every 5 min
 *   2. Detects patterns: ad-hoc actions repeating 2+ times across distinct contexts
 *   3. Auto-drafts a SKILL.md to seeds/skills/_drafts/{name}/
 *   4. Logs the proposal to growth-log.md
 *   5. Queues a Telegram approval card for Jack
 *
 * Growth-always-on default: 2-occurrence rule (relaxed from skill-promotion-criteria's 3).
 *
 * Run: node ~/Documents/studio/scripts/growth-watcher.mjs
 * Or install as LaunchAgent: bash scripts/install-growth-watcher.sh
 */

import fs from "node:fs/promises";
import path from "node:path";
import { existsSync } from "node:fs";
import { homedir } from "node:os";

// ---- paths ----
const SHARED_DIR = path.join(homedir(), "Documents/businesses/_shared");
const GROWTH_DIR = path.join(SHARED_DIR, "growth");
const REGISTER_PATH = path.join(GROWTH_DIR, "work-register.jsonl");
const GROWTH_LOG = path.join(GROWTH_DIR, "growth-log.md");
const DRAFTS_DIR = path.join(
  homedir(),
  "Documents/studio/docs/seeds/skills/_drafts"
);
const META_DRAFTS_DIR = path.join(
  homedir(),
  "Documents/studio/docs/seeds/skills/_drafts/_meta"
);
const META_GAPS_LOG = path.join(
  homedir(),
  "Documents/businesses/_shared/growth/meta-gaps.md"
);
const META_CIRCUIT_FILE = path.join(
  homedir(),
  "Documents/businesses/_shared/growth/meta-circuit-state.json"
);
const POLLER_DIR = path.join(SHARED_DIR, "poller");
const HEARTBEAT_FILE = path.join(POLLER_DIR, "growth-watcher-heartbeat.log");
const SEEN_FILE = path.join(GROWTH_DIR, "growth-watcher-seen.json");
const TELEGRAM_OUTBOX = path.join(SHARED_DIR, "telegram/outbox");
const ENV_FILE = path.join(homedir(), "Documents/studio/.env.local");

const POLL_INTERVAL_MS = 5 * 60_000; // every 5 min
const HEARTBEAT_INTERVAL_MS = 60_000;

// ---- env ----
async function loadEnv() {
  if (!existsSync(ENV_FILE)) return {};
  const text = await fs.readFile(ENV_FILE, "utf8");
  const env = {};
  for (const line of text.split("\n")) {
    const m = line.match(/^\s*([A-Z_]+)\s*=\s*(.*)\s*$/);
    if (m && !line.trim().startsWith("#")) {
      env[m[1]] = m[2].replace(/^['"]|['"]$/g, "");
    }
  }
  return env;
}

// ---- logging ----
async function log(msg) {
  const stamp = new Date().toISOString();
  const line = `[${stamp}] ${msg}\n`;
  process.stdout.write(line);
  await fs.appendFile(path.join(POLLER_DIR, "growth-watcher.log"), line);
}

async function heartbeat() {
  await fs.appendFile(
    HEARTBEAT_FILE,
    `${new Date().toISOString()} alive\n`
  );
}

// ---- ensure dirs ----
async function ensureDirs() {
  for (const d of [
    GROWTH_DIR,
    DRAFTS_DIR,
    META_DRAFTS_DIR,
    POLLER_DIR,
    TELEGRAM_OUTBOX,
  ]) {
    await fs.mkdir(d, { recursive: true });
  }
  if (!existsSync(REGISTER_PATH)) {
    await fs.writeFile(REGISTER_PATH, "", "utf8");
  }
  if (!existsSync(GROWTH_LOG)) {
    await fs.writeFile(
      GROWTH_LOG,
      "# Growth log\n\n> Auto-generated by growth-watcher.\n> Every detected pattern gets a row.\n\n",
      "utf8"
    );
  }
  if (!existsSync(META_GAPS_LOG)) {
    await fs.writeFile(
      META_GAPS_LOG,
      "# Meta-gaps log\n\n> Auto-generated by growth-watcher (recursive layer).\n> Patterns detected inside the growth cluster itself.\n\n",
      "utf8"
    );
  }
  if (!existsSync(META_CIRCUIT_FILE)) {
    await fs.writeFile(
      META_CIRCUIT_FILE,
      JSON.stringify(
        { open: false, since: null, reason: null, drafts_in_flight: 0 },
        null,
        2
      ),
      "utf8"
    );
  }
}

// ---- throttle: read circuit breaker state ----
async function readMetaCircuit() {
  try {
    return JSON.parse(await fs.readFile(META_CIRCUIT_FILE, "utf8"));
  } catch {
    return { open: false, since: null, reason: null, drafts_in_flight: 0 };
  }
}

async function writeMetaCircuit(state) {
  await fs.writeFile(META_CIRCUIT_FILE, JSON.stringify(state, null, 2), "utf8");
}

// ---- count meta-drafts currently in flight ----
async function countMetaDraftsInFlight() {
  try {
    const entries = await fs.readdir(META_DRAFTS_DIR);
    let count = 0;
    for (const e of entries) {
      const p = path.join(META_DRAFTS_DIR, e, "SKILL.md");
      if (existsSync(p)) count += 1;
    }
    return count;
  } catch {
    return 0;
  }
}

// ---- throttle gate: returns {allow, reason} ----
async function throttleAllowMetaDraft() {
  const state = await readMetaCircuit();
  if (state.open) {
    return { allow: false, reason: `circuit_open: ${state.reason || "unknown"}` };
  }
  const inFlight = await countMetaDraftsInFlight();
  if (inFlight >= 3) {
    return { allow: false, reason: `queue_full: ${inFlight} drafts in flight` };
  }
  return { allow: true, reason: null };
}

// ---- seen state ----
async function loadSeen() {
  if (!existsSync(SEEN_FILE)) return { proposed_phrases: [] };
  try {
    return JSON.parse(await fs.readFile(SEEN_FILE, "utf8"));
  } catch {
    return { proposed_phrases: [] };
  }
}

async function saveSeen(seen) {
  await fs.writeFile(SEEN_FILE, JSON.stringify(seen, null, 2), "utf8");
}

// ---- read register ----
async function readRegister() {
  const text = await fs.readFile(REGISTER_PATH, "utf8");
  return text
    .split("\n")
    .filter((l) => l.trim())
    .map((l) => {
      try {
        return JSON.parse(l);
      } catch {
        return null;
      }
    })
    .filter(Boolean);
}

// ---- normalize phrase for matching ----
function normalizePhrase(s) {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

// ---- group by normalized action ----
// `metaMode`: when true, only consider is_meta=true entries (and inverse otherwise)
function groupAdHocByPhrase(entries, metaMode = false) {
  const groups = new Map();
  for (const entry of entries) {
    if (!entry.is_ad_hoc) continue;
    if (entry.invoked_skill) continue; // skill exists already
    const entryIsMeta = entry.is_meta === true;
    if (metaMode && !entryIsMeta) continue;
    if (!metaMode && entryIsMeta) continue;
    const key = normalizePhrase(entry.action_phrase || "");
    if (!key) continue;
    if (!groups.has(key)) {
      groups.set(key, {
        phrase: entry.action_phrase,
        occurrences: [],
      });
    }
    groups.get(key).occurrences.push(entry);
  }
  return groups;
}

// ---- detect promotion candidates ----
// `minOccurrences` defaults to 2 (domain) — pass 3 for meta layer.
function detectCandidates(groups, seenPhrases, minOccurrences = 2) {
  const candidates = [];
  for (const [normalized, group] of groups) {
    if (seenPhrases.includes(normalized)) continue;
    if (group.occurrences.length < minOccurrences) continue;

    // Require distinct contexts (not N ad-hocs in the same session)
    const distinctContexts = new Set(
      group.occurrences.map((o) => o.context || "default")
    );
    if (distinctContexts.size < minOccurrences) continue;

    candidates.push({
      normalized,
      phrase: group.phrase,
      occurrences: group.occurrences,
      contexts: Array.from(distinctContexts),
    });
  }
  return candidates;
}

// ---- generate a skill name from a phrase ----
function generateSkillName(phrase) {
  return phrase
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, "")
    .split(/\s+/)
    .slice(0, 4) // first 4 words
    .filter(Boolean)
    .join("-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 50);
}

// ---- draft a SKILL.md ----
